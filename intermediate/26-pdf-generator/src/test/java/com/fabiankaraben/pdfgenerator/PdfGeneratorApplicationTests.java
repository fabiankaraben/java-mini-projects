package com.fabiankaraben.pdfgenerator;

import org.apache.pdfbox.pdmodel.PDDocument;
import org.apache.pdfbox.text.PDFTextStripper;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.web.servlet.AutoConfigureMockMvc;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.http.MediaType;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.test.web.servlet.MvcResult;

import java.io.ByteArrayInputStream;

import static org.assertj.core.api.Assertions.assertThat;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.get;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.content;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.header;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

@SpringBootTest
@AutoConfigureMockMvc
class PdfGeneratorApplicationTests {

    @Autowired
    private MockMvc mockMvc;

    @Test
    void shouldGeneratePdfWithDefaultContent() throws Exception {
        MvcResult result = mockMvc.perform(get("/generate-pdf"))
                .andExpect(status().isOk())
                .andExpect(content().contentType(MediaType.APPLICATION_PDF))
                .andExpect(header().string("Content-Disposition", "form-data; name=\"attachment\"; filename=\"generated.pdf\""))
                .andReturn();

        byte[] pdfBytes = result.getResponse().getContentAsByteArray();
        
        try (PDDocument document = PDDocument.load(new ByteArrayInputStream(pdfBytes))) {
            PDFTextStripper stripper = new PDFTextStripper();
            String pdfText = stripper.getText(document);

            assertThat(pdfText).contains("Sample PDF");
            assertThat(pdfText).contains("This is a sample PDF generated by OpenPDF.");
        }
    }

    @Test
    void shouldGeneratePdfWithCustomContent() throws Exception {
        String customTitle = "Custom Report";
        String customContent = "This is a custom report content.";

        MvcResult result = mockMvc.perform(get("/generate-pdf")
                        .param("title", customTitle)
                        .param("content", customContent))
                .andExpect(status().isOk())
                .andExpect(content().contentType(MediaType.APPLICATION_PDF))
                .andReturn();

        byte[] pdfBytes = result.getResponse().getContentAsByteArray();

        try (PDDocument document = PDDocument.load(new ByteArrayInputStream(pdfBytes))) {
            PDFTextStripper stripper = new PDFTextStripper();
            String pdfText = stripper.getText(document);

            assertThat(pdfText).contains(customTitle);
            assertThat(pdfText).contains(customContent);
        }
    }
}
